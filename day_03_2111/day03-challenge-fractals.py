# - parameter #1 - read a list of strings - each string with exactly the same number of characters
# - parameter #2 - n - iteration number
# print the input, but "fractaled" 3x3 n times, with dots '.' in the middle, e.g.:

# input:
#  ###
#  #-#     , 2
#  ###
# output:
#  #########
#  #-##-##-#
#  #########
#  ###...###
